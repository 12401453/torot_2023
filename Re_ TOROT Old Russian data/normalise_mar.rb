def normal_mar(tk)
  lt = []
  chs = tk.split(//u)
  chs.each do |c|
    if MNORMAL.keys.include?(c)
      lt <<  MNORMAL[c]
    else 
      lt << c
    end
  end 
  return lt.join
end
