def normal_rus(tk)
  lt = []
  chs = tk.split(//u)
  chs.each do |c|
    if RUSNORMAL.keys.include?(c)
      lt <<  RUSNORMAL[c]
    else 
      lt << c
    end
  end 
  return lt.join.gsub(/оу/,'у')
end
